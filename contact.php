<script>
    /*==============================================================*/
    // Fria Contact Form  JS
    /*==============================================================*/
    (function($) {
        "use strict"; // Start of use strict
        $("#contactForm1").validator().on("submit", function(event) {
            if (event.isDefaultPrevented()) {
                // handle the invalid form...
                formError1();
                submitMSG1(false, "Did you fill in the form properly?");
            } else {
                // everything looks good!
                event.preventDefault();
                submitForm1();
            }
        });


        function submitForm1() {
            // Initiate Variables With Form Content
            var name = $("#name1").val();
            var email = $("#email1").val();
            var msg_subject = $("#msg_subject1").val();
            var phone_number = $("#phone_number1").val();
            var message = $("#message1").val();


            $.ajax({
                type: "POST",
                url: "includes/ajax",
                data: "contactForm=" + true + "&name=" + name + "&email=" + email + "&msg_subject=" + msg_subject + "&phone_number=" + phone_number + "&message=" + message,
                success: function(text) {
                    // console.log(text); return false;
                    if (text == "success") {
                        formSuccess1();
                    } else {
                        formError1();
                        submitMSG1(false, text);
                    }
                }
            });
        }

        function formSuccess1() {
            $("#contactForm1")[0].reset();
            submitMSG1(true, "Message Submitted!")
        }

        function formError1() {
            $("#contactForm1").removeClass().addClass('shake animated').one('webkitAnimationEnd mozAnimationEnd MSAnimationEnd oanimationend animationend', function() {
                $(this).removeClass();
            });
        }

        function submitMSG1(valid, msg) {
            if (valid) {
                var msgClasses = "h4 tada animated text-success";
            } else {
                var msgClasses = "h4 text-danger";
            }
            $("#msgSubmit1").removeClass().addClass(msgClasses).text(msg);
        }
    }(jQuery)); // End of use strict
</script>


